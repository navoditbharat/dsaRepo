public class BasicString {

	public static void main(String[] args) {

		String str = "Hello";

		System.out.println(str);
		System.out.println(str.charAt(0));
		System.out.println(str.charAt(str.length() - 1));
		System.out.println(str.charAt(1));

	}

}
